package com.example.indoorsgps;

public class BuildingModel {

    private String id;
    private String latitude;
    private String longitude;
    private String radius;

    public String getId() {
        return id;
    }

    public String getLatitude() {
        return latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public String getRadius() {
        return radius;
    }
}
