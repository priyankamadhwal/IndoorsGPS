package com.example.indoorsgps;

public class BuildingModel {

    private String id;
    private double latitude;
    private double longitude;
    private float radius;

    public String getId() {
        return id;
    }

    public double getLatitude() {
        return latitude;
    }

    public double getLongitude() {
        return longitude;
    }

    public float getRadius() {
        return radius;
    }
}
